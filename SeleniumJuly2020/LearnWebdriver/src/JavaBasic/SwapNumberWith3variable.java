package JavaBasic;

public class SwapNumberWith3variable {

	public static void main(String[] args) {
		int a=10;
		int b=5;
		int temp=0;
		
		temp= a;  //--->temp =10
		a= b;    //---->a=5
		b= temp; //  ---->b=10

		System.out.println("value of a is :" +a);
		System.out.println("value of a is :" +b);
	}

}
